    /// Returns true when the calling test should attempt a real network download.
    /// Reads <c>AIDOTNET_RUN_DATASET_DOWNLOAD_TESTS</c> at the moment of the call.
    /// </summary>
    public static bool IsNetworkTestEnabled =>
        string.Equals(
            Environment.GetEnvironmentVariable("AIDOTNET_RUN_DATASET_DOWNLOAD_TESTS"),
            "1",
            StringComparison.Ordinal);

    /// <summary>
    /// Allocates a fresh temp directory for the duration of a test. Caller is
    /// responsible for invoking <see cref="TryCleanup"/> in a <c>finally</c>.
    /// </summary>
    public static string CreateTempDir(string prefix)
    {
        string root = Path.Combine(
            Path.GetTempPath(),
            $"aidotnet-{prefix}-{Guid.NewGuid():N}");
        Directory.CreateDirectory(root);
        return root;
    }

    /// <summary>Best-effort recursive cleanup; never throws.</summary>
    public static void TryCleanup(string path)
    {
        if (string.IsNullOrEmpty(path)) return;
        try { if (Directory.Exists(path)) Directory.Delete(path, recursive: true); }
        catch { /* test-cleanup failures are non-fatal */ }
    }

    /// <summary>
    /// Asserts a tensor's shape matches the expected dimensions.
    /// </summary>
    public static void AssertShape<T>(Tensor<T> t, params int[] expected)
    {
        var actual = t._shape;
        Assert.True(actual.Length == expected.Length,
            $"Expected rank {expected.Length}, got {actual.Length} (shape [{string.Join(",", actual)}])");
        for (int i = 0; i < expected.Length; i++)
            Assert.True(actual[i] == expected[i],
                $"Dim {i}: expected {expected[i]}, got {actual[i]} (shape [{string.Join(",", actual)}])");
    }

    /// <summary>
    /// Writes a 16-bit PCM mono WAV with <paramref name="sampleCount"/> zero samples
    /// at the given <paramref name="sampleRate"/>. Used by audio-loader fixtures.
    /// </summary>
    public static byte[] CreateMockWav(int sampleCount, int sampleRate = 22050)
    {
        int dataBytes = sampleCount * 2; // 16-bit mono
        int fileBytes = 36 + dataBytes;
        var ms = new MemoryStream();
        using var bw = new BinaryWriter(ms);
        // RIFF header
        bw.Write(System.Text.Encoding.ASCII.GetBytes("RIFF"));
        bw.Write(fileBytes);
        bw.Write(System.Text.Encoding.ASCII.GetBytes("WAVE"));
        // fmt chunk
        bw.Write(System.Text.Encoding.ASCII.GetBytes("fmt "));
        bw.Write(16);                      // chunk size
        bw.Write((short)1);                // PCM
        bw.Write((short)1);                // mono
        bw.Write(sampleRate);              // sample rate
        bw.Write(sampleRate * 2);          // byte rate
        bw.Write((short)2);                // block align
        bw.Write((short)16);               // bits per sample
        // data chunk
        bw.Write(System.Text.Encoding.ASCII.GetBytes("data"));
        bw.Write(dataBytes);
        // payload (silence)
        for (int i = 0; i < dataBytes; i++) bw.Write((byte)0);
        bw.Flush();
        return ms.ToArray();
    }

