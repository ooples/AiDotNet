namespace AiDotNet.Postprocessing.Document;

/// <summary>
/// Data types for parsed document values.
/// </summary>
public enum DataType
{
    /// <summary>
    /// String type.
    /// </summary>
    String,

    /// <summary>
    /// Integer type.
    /// </summary>
    Integer,

    /// <summary>
    /// Decimal/floating point type.
    /// </summary>
    Decimal,

    /// <summary>
    /// Boolean type.
    /// </summary>
    Boolean,

    /// <summary>
    /// Date/time type.
    /// </summary>
    DateTime
}
